package com.harbor.dto;

public class DepartmentDto {

	private String dpt_name;
	private String dpt_location;
	private String hid;
	private String dpt_id;

	public String getDpt_name() {
		return dpt_name;
	}

	public void setDpt_name(String dpt_name) {
		this.dpt_name = dpt_name;
	}

	public String getDpt_location() {
		return dpt_location;
	}

	public void setDpt_location(String dpt_location) {
		this.dpt_location = dpt_location;
	}

	public String getHid() {
		return hid;
	}

	public void setHid(String hid) {
		this.hid = hid;
	}

	public String getDpt_id() {
		return dpt_id;
	}

	public void setDpt_id(String dpt_id) {
		this.dpt_id = dpt_id;
	}

	@Override
	public String toString() {
		return "DepartmentDto [dpt_name=" + dpt_name + ", dpt_location=" + dpt_location + ", hid=" + hid + ", dpt_id="
				+ dpt_id + "]";
	}
	
	
	

}
